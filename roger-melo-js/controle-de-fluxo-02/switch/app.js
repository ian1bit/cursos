// switch statement
const grade = 'B'

switch (grade) {
  case 'A':
    console.log('voce tirou um A')
    break
  case 'B':
    console.log('voce tirou um B')
    break
  case 'C':
    console.log('voce tirou um C')
    break
  case 'D':
    console.log('voce tirou um D')
    break
  case 'E':
    console.log('voce tirou um E')
    break
  default:
    console.log('nota invalida');
}
